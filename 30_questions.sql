CREATE FUNCTION udf_fetch_data
(@prod NVARCHAR(50))
RETURNS NVARCHAR(50)
AS BEGIN
   DECLARE @type NVARCHAR(50)

   SELECT @type=Product_Type
   FROM Product
   WHERE Product=@prod

   RETURN @type
END
